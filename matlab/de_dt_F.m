function de_dt = de_dt_F(s,t,p)

%%   d(entropy)/dt from twice differentiating the Gibbs potential in
%%   Feistel (2003), Prog. Ocean. 58, 43-114
%%
%%   usage        : de_dt = de_dt_F(s,t,p)
%%
%%   s            : salinity                           (psu)
%%   t            : in-situ temperature                (deg C, ITS-90)
%%   p            : gauge pressure                     (dbar)
%%                  (absolute pressure - 10.1325 dbar)
%%
%%   de_dt        : d(entropy)/dt                      (kJ/kgK^2)
%%
%%   calls        : zcheck
%%
%%   check value  : de_dt_F(35,20,4000) = 13.3502201137063

%%   DRJ on 12/10/03


zcheck(s,t,p)


x2 = 2.5d-2*s; x = sqrt(x2); y = 2.5d-2*t; z = 1.0d-4*p;

de_dt = 24715.571866078 + x2.*(-1858.920033948178 + ...
                     x.*(317.440355256842 + y.*(-405.1392883572282 + ...
                     202.6815298758072*y)) + y.*(1562.563716288858 + ...
                     y.*(-1165.8752731900836 + 348.7487684426*y))) + ...
                   y.*(-4420.4472249096725 + y.*(1778.231237203896 + ...
                       y.*(-1160.5182516851419 + (569.531539542516 - ...
                                       128.13429152494615*y).*y))) + ...
                     z.*(-2910.0729080936 + x2.*(1721.528607567954 + ...
                   y.*(-4165.4688847996085 + 3572.7449038462437*y) + ...
                       x.*(-766.116132004952 + (2761.9195908075417 - ...
                   2814.78225133626*y).*y)) + y.*(4035.04669887042 + ...
                      y.*(-4775.621344883664 + y.*(3892.3662123519 + ...
                 y.*(-1905.341809925355 + 404.50541014508605*y)))) + ...
                    z.*(1513.116771538718 + x2.*(-674.819060538734 + ...
                          108.3834525034224*x + (1229.337851789418 - ...
                  896.713693665072*y).*y) + y.*(-2996.162344914912 + ...
                      y.*(3621.784567462512 + y.*(-2410.4130980405 + ...
                     668.691951421377*y))) + z.*(-546.959324647056 + ...
                        x2.*(356.629112415276 - 51.2796974779828*x + ...
                    y.*(-681.370187043564 + 437.84750280190565*y)) + ...
                   y.*(1437.2719839264719 + y.*(-1826.356460806092 + ...
                   (1105.446104680304 - 245.11816254543362*y).*y)) + ...
                       z.*(111.1208127634436 + x2.*(-88.4080716616 + ...
                      66.7696405958478*y) + y.*(-292.8075111563232 + ...
                    (316.49805267936244 - 129.6381336154442*y).*y) + ...
                         (-8.68841343834394 + 15.84003094423364*x2 + ...
                                       9.978426372534301*y).*z))));


de_dt = 6.25d-4*de_dt;


return
